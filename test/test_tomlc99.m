%----------------------------------------------------------------------------%
% Tests tomlc99 bindings.
%----------------------------------------------------------------------------%

:- module test_tomlc99.
:- interface.

:- import_module io.

%----------------------------------------------------------------------------%

:- pred main(io::di, io::uo) is det.

%----------------------------------------------------------------------------%
%----------------------------------------------------------------------------%

:- implementation.

:- import_module tomlc99.
:- import_module maybe.

%----------------------------------------------------------------------------%

main(!IO) :-
  toml_parse_file("does_not_exit.toml", Res, !IO),
  (
    Res = ok(_), S = "passed";
    Res = error(S)
  ),
  io.write_string(S, !IO).
